package homework.lesson3.task1;

public class Calculator {
    /**
     * Сложение целых чисел
     *
     * @return результат сложения a и b
     */
    int addition(int a, int b) {
        return a + b;
    }

    /**
     * Вычитнае целых чисел
     *
     * @return результат вычитания b из a
     */
    int subtraction(int a, int b) {
        return a - b;
    }

    /**
     * Умножение целых чисел
     *
     * @return результат множения a на b
     */
    int multiplication(int a, int b) {
        return a * b;
    }

    /**
     * Деление целых чисел
     *
     * @return результат деления a на b
     */
    int separation(int a, int b) {
        return a / b;
    }
}
